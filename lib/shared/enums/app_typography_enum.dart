/// [AppTypographyType] is used to change the type of GFTypography title widget.
enum AppTypographyType {
  /// [AppTypographyType.typo1] type used for title widget with fontSize = 25.0
  typo1,

  /// [AppTypographyType.typo2] type used for title widget with fontSize = 22.0
  typo2,

  /// [AppTypographyType.typo3] type used for title widget with fontSize = 19.0
  typo3,

  /// [AppTypographyType.typo4] type used for title widget with fontSize = 17.0
  typo4,

  /// [AppTypographyType.typo5] type used for title widget with fontSize = 15.0
  typo5,

  /// [AppTypographyType.typo6] type used for title widget with fontSize = 13.0
  typo6,
}
